package com.Bolang.Object_Game;

public class Gap {
    private int gapWidth;
    private int gapHeight;

    public Gap(int height, int width) {
        this.gapHeight = height;
        this.gapWidth = width;
    }

    public int getGapWidth() {
        return gapWidth;
    }
    public int getGapHeight() {
        return gapHeight;
    }
}
